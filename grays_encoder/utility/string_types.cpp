// HashedStringT
//------------------------------------------------------------------------------
//------------------------------------------------------------------------------

//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
HashedStringT::HashedStringT( const char cChar )
{
	Set( cChar );
	Set( cChar );
}

//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
HashedStringT::HashedStringT( const char* cStr )
{
	Set( cStr );
}

//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
HashedStringT::HashedStringT( const std::string& str )
{
	Set( str );
}

//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
HashingString::HashedStringT( const HashedStringT& str )
{
	Set( str );
}

//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
size_t HashingString::Hash() const
{
	return m_hash;
}

//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
const std::string& HashedStringT::String() const
{
	return m_string;
}

//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
HashingString& HashingString::operator=( const std::string& str )
{
	Set( str );
	return *this;
}

//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
HashingString& HashingString::operator=( const HashingString& str )
{
	Set( str );
	return *this;
}

//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
bool HashingString::operator==( const HashingString& str ) const
{
	return str.m_hash == m_hash;
}

//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
bool HashingString::operator==( const std::string& str ) const
{
	size_t otherHash = std::hash<std::string>{}(str);
	return m_hash == otherHash;
}

//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
HashingString& HashingString::operator+=( const char cChar )
{
	m_string += cChar;
	m_hash = std::hash<std::string>{}(m_string);
	return *this;
}

//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
HashingString& HashingString::operator+=( const char* cStr )
{
	m_string.append( cStr );
	m_hash = std::hash<std::string>{}(m_string);
	return *this;
}

//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
HashingString& HashingString::operator+=( const std::string& stdStr )
{
	m_string.append( stdStr );
	m_hash = std::hash<std::string>{}(m_string);
	return *this;
}

//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
HashingString& HashingString::operator+=( const HashingString& hashStr )
{
	m_string.append( hashStr.m_string );
	m_hash = std::hash<std::string>{}(m_string);
	return *this;
}

//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
void HashingString::Set( const char chr )
{
	m_string.clear();
	m_string += chr;
	m_hash = std::hash<std::string>{}(m_string);
}

//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
void HashingString::Set( const char* str )
{
	m_string = str;
	m_hash = std::hash<std::string>{}(m_string);
}

//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
void HashingString::Set( const std::string& str )
{
	m_string = str;
	m_hash = std::hash<std::string>{}(m_string);
}

//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
void HashingString::Set( const HashingString& str )
{
	m_string = str.m_string;
	m_hash = str.m_hash;
}